export function Icon({
  name,
  className = "w-[19px] h-[19px]",
}: {
  name: string;
  className?: string;
}): ReactNode {
  // Select icon set based on active layout
  const layout = typeof document !== "undefined"
    ? document.documentElement.getAttribute("data-layout") || "command"
    : "command";
  const setName = layout === "glass" ? "glass" : layout === "atlas" ? "atlas" : "command";
  const set = iconSets[setName] || iconSets.command;
  const icon = set[name] || set["dashboard"];
  if (!icon) return null;

  if (icon.fill) {
    // Atlas solid icons
    return createElement(
      "svg",
      {
        className,
        viewBox: "0 0 24 24",
        fill: "currentColor",
        "aria-hidden": "true",
      },
      ...icon.paths.map((d, i) =>
        createElement("path", { key: i, d })
      )
    );
  }

  // Command/Glass outline icons
  return createElement(
    "svg",
    {
      className,
      fill: "none",
      viewBox: "0 0 24 24",
      stroke: "currentColor",
      strokeWidth: icon.strokeWidth || 1.5,
      "aria-hidden": "true",
    },
    ...icon.paths.map((d, i) =>
      createElement("path", {
        key: i,
        strokeLinecap: "round",
        strokeLinejoin: "round",
        d,
      })
    )
  );
}
